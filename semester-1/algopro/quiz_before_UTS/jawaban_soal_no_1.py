Input_user_1 = int(input("masukkan angka pertama : "))
Input_user_2 = int(input("masukkan angka kedua : "))

def perkalian(Input_user_1, Input_user_2):
    return Input_user_1 * Input_user_2

def penjumlahan(Input_user_1, Input_user_2):
    return Input_user_1 + Input_user_2

def pengurangan(Input_user_1, Input_user_2):
    return Input_user_1 - Input_user_2

def pembagian(Input_user_1, Input_user_2):
    return Input_user_1 / Input_user_2

def sisabagi(Input_user_1, Input_user_2):
    return Input_user_1 % Input_user_2

print(pembagian(Input_user_1, Input_user_2))
